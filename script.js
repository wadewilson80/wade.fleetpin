// ------- Render helpers -------
const $ = (sel) => document.querySelector(sel);
const tilesEl = $("#tiles");
const quotesEl = $("#quotes");

$("#name").textContent = data.name;
$("#targetRole").textContent = data.targetRole;
$("#pitch").textContent = data.pitch;
$("#updatedChip").textContent = data.updated;
$("#noteChip").textContent = data.note;
$("#footerLeft").textContent = data.footerLeft;

$("#btnCv").href = data.links.cvPdf;
$("#btnLinkedIn").href = data.links.linkedIn;
$("#btnEmail").href = `mailto:${data.links.email}`;

$("#quoteNote").textContent = data.quoteNote;

function safeText(str){ return String(str ?? ""); }

function renderTiles(){
  tilesEl.innerHTML = "";
  data.capabilities.forEach((c, idx) => {
    const btn = document.createElement("button");
    btn.className = "tile";
    btn.type = "button";
    btn.setAttribute("data-id", c.id);
    btn.setAttribute("aria-label", `${c.title}. Click for details.`);
    btn.innerHTML = `
      <div class="tileTop">
        <h3 class="tileTitle">${safeText(c.title)}</h3>
        <div class="badge">${safeText(c.tag || `Tile ${idx+1}`)}</div>
      </div>
      <div class="tileProof"><strong>Proof:</strong> ${safeText(c.hoverProof)}</div>
      <div class="tileFade" aria-hidden="true"></div>
    `;
    btn.addEventListener("click", () => openPanel(c.id));
    tilesEl.appendChild(btn);
  });
}

function renderQuotes(){
  quotesEl.innerHTML = "";
  data.testimonials.forEach(q => {
    const card = document.createElement("div");
    card.className = "quoteCard";
    card.innerHTML = `
      <blockquote>“${safeText(q.text)}”</blockquote>
      <div class="by">— ${safeText(q.by)}</div>
    `;
    quotesEl.appendChild(card);
  });
}

function openPanel(id){
  const c = data.capabilities.find(x => x.id === id);
  if(!c) return;

  $("#panelTitle").textContent = c.title;
  $("#panelSub").textContent = c.sub || "";

  const bullets = $("#panelBullets");
  bullets.innerHTML = "";
  (c.bullets || []).forEach(b => {
    const li = document.createElement("li");
    li.textContent = b;
    bullets.appendChild(li);
  });

  const outcomesBlock = $("#panelOutcomeBlock");
  const outcomes = $("#panelOutcomes");
  outcomes.innerHTML = "";
  if(c.outcomes && c.outcomes.length){
    outcomesBlock.style.display = "";
    c.outcomes.forEach(o => {
      const li = document.createElement("li");
      li.textContent = o;
      outcomes.appendChild(li);
    });
  } else {
    outcomesBlock.style.display = "none";
  }

  const quoteBlock = $("#panelQuoteBlock");
  if(c.quote && c.quote.text){
    quoteBlock.style.display = "";
    $("#panelQuote").textContent = `“${c.quote.text}”`;
    $("#panelQuoteBy").textContent = c.quote.by ? `— ${c.quote.by}` : "";
  } else {
    quoteBlock.style.display = "none";
  }

  const linksBlock = $("#panelLinksBlock");
  const linksEl = $("#panelLinks");
  linksEl.innerHTML = "";
  if(c.links && c.links.length){
    linksBlock.style.display = "";
    c.links.forEach(l => {
      const a = document.createElement("a");
      a.className = "linkPill";
      a.href = l.href;
      a.target = "_blank";
      a.rel = "noopener";
      a.textContent = l.label || "Link";
      linksEl.appendChild(a);
    });
  } else {
    linksBlock.style.display = "none";
  }

  // Scroll panel into view on mobile
  $("#panel").scrollIntoView({behavior:"smooth", block:"start"});
}

$("#closePanel").addEventListener("click", () => {
  $("#panelTitle").textContent = "Select a tile";
  $("#panelSub").textContent = "Details will appear here.";
  $("#panelBullets").innerHTML = "";
  $("#panelOutcomeBlock").style.display = "none";
  $("#panelQuoteBlock").style.display = "none";
  $("#panelLinksBlock").style.display = "none";
});

$("#printBtn").addEventListener("click", () => window.print());

renderTiles();
renderQuotes();
